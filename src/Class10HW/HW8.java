package Class10HW;
//Create an array to store char values and then print those in reverse order
public class HW8 {
    public static void main(String[] args) {
        char [] vals={'a','b','c','d','e'};
        for(int i= vals.length -1; i>=0; i--){
            System.out.print(vals[i]);

        }

    }
}
